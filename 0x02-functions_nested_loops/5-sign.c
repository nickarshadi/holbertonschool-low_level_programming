#include "holberton.h"
/**
 * print_sign - print a sign
 * Return: return value depends on variable
 * @n: function parameter
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar('0');
		return (0);
	}
	else
	{
		_putchar('-');
		return (-1);
	}
}
